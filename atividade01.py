print('Atividade')

print('Cadastre os números:')

num1 = int(input('Numero 1: '))
num2 = int(input('Numero 2: '))

print('================================')

print("Retorno dos dados inseridos!")
print('Numero 1 inserido: ', num1)
print('Numero 2 inserido: ', num2)

print('================================')

print('Cálculo')
print('Soma dos números: ', num1 + num2)
print('Subtração dos números: ', num1 - num2)
print('Multiplicação dos números: ', num1 * num2)
print('Divisão dos números: ', num1 / num2)

print('================================')

print('Maior, igual, menor')
print('Os dois números inseridos são iguais: ', num1 == num2)
print('O primeiro número inserido é maior que o segundo: ', num1 > num2)
print('Um dos números é maior que zero: ', num1 > 10 or num2 > 10)
print('O número 1 é maior que zero: ', num1 > 10)
print('O número 2 é maior que zero: ', num2 > 10)


